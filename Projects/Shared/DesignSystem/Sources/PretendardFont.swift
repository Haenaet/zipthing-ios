import UIKit

public enum PretendardFont {
    public static let familyName = "Pretendard"
    
    public enum Weight: String {
        case black = "Black"
        case bold = "Bold"
        case extraBold = "ExtraBold"
        case light = "Light"
        case medium = "Medium"
        case regular = "Regular"
        case semiBold = "SemiBold"
        case thin = "Thin"
    }
}
